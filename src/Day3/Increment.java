package Day3;

public class Increment {

	public static void main(String[] args) {
		int a=10;
		int b=20;
		int c=30;
		int d=40;
		System.out.println("a+b="+(a+b));
		System.out.println("a-b="+(a-b));
		System.out.println("a*b="+(a*b));
		System.out.println("c/b="+(c/b));
		System.out.println("d%b="+(d%b));
		System.out.println("a++="+(a++));
		System.out.println("++a="+(++a));
		System.out.println("a-="+(a--));
		System.out.println("a+b="+(a+b));


	}

}
